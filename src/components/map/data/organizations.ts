// Функция для преобразования старой структуры в новую
function createOrganization(
	id: string | number,
	name: string,
	cityName: string,
	typeName: string,
	assistance: string[],
	summary: string,
	description: string,
	mission: string,
	goals: string[],
	needs: string[],
	coordinates: [number, number],
	address: string,
	contacts: { phone: string; email?: string },
	gallery: string[]
): Organization {
	const city = cityMap[cityName] || {
		id: 999,
		name: cityName,
		latitude: coordinates[0].toString(),
		longitude: coordinates[1].toString(),
	}

	const orgType = organizationTypeMap[typeName] || {
		id: 999,
		name: typeName,
	}

	const helpTypes = assistance
		.map(aid => helpTypeMap[aid])
		.filter(Boolean) as Array<{ id: number; name: string }>

	const contactList = [
		{ name: 'Телефон', value: contacts.phone },
		...(contacts.email ? [{ name: 'Email', value: contacts.email }] : []),
	]

	return {
		id: typeof id === 'string' ? id : id,
		name,
		latitude: coordinates[0].toString(),
		longitude: coordinates[1].toString(),
		summary,
		mission,
		description,
		goals,
		needs,
		address,
		contacts: contactList,
		organizationTypes: [orgType],
		gallery,
		city,
		helpTypes,
		createdAt: new Date().toISOString(),
		updatedAt: new Date().toISOString(),
	}
}

